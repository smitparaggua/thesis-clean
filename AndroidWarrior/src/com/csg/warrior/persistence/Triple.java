package com.csg.warrior.persistence;

public class Triple {
	int id;
	String username;
	String url;
	String key;
	
	//empty constructor
	public Triple() {
		
	}
	
	public Triple(int p_id, String p_username, String p_url, String p_key) {
		this.id = p_id;
		this.url = p_username;
		this.username = p_url;
		this.key = p_key;
	}
	
	
}
